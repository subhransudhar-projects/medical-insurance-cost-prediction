w("- `age`: fairly uniform/flat spread across the adult range (18–64) with a mild concentration of "
  "18-year-olds (a data-collection artifact common in this dataset — enrollment often starts at 18) — not a "
  "classic bell curve, closer to uniform. This tells us the sample spans working-age adults broadly rather than skewing toward one age group.")
w("- `bmi`: close to a normal (bell-shaped) distribution, centered around 30 (which is itself in the "
  "'obese' clinical range) — the population sampled skews toward higher BMI on average than a general "
  "healthy-weight population.")
w("- `children`: heavily right-skewed / count-like, most individuals have 0–2 children, few have 4–5 — "
  "expected for a discrete, bounded count variable.")
w("- `charges`: strongly right-skewed with a long tail of high-cost cases — median (\"typical\" cost) is "
  "well below the mean, meaning a relatively small number of expensive cases pull the average up. This is "
  "the classic shape of insurance cost data and is the reason a log-transform is often used for modeling "
  "(see Chunk 5).\n")

