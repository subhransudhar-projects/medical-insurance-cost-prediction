fig.text(0.98, 0.01, f"Strongest: {strongest_pair[0]} ↔ {strongest_pair[1]} (r = {corr.loc[strongest_pair]:.2f})",
          ha="right", va="bottom", fontsize=9, color="dimgrey", style="italic")
