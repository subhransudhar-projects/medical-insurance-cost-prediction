def w(s=""):
    lines.append(str(s))

def df_to_md(d, index=True):
    return d.to_markdown(index=index)

def save(fig, name):
    fig.savefig(FIGDIR / f"{name}.pdf", dpi=150, bbox_inches="tight")
    fig.savefig(FIGDIR / f"{name}.png", dpi=150, bbox_inches="tight")
    plt.close(fig)

